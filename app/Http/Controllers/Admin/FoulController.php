    public function addFilter($request)
    {
        $this->query->with('event', 'event.sportDiscipline', 'event.sportDiscipline.sport');

        if ($request->sport_discipline_id) {
            $this->query->whereHas('event.sportDiscipline', function ($query) use ($request) {
                $query->where('id', $request->sport_discipline_id);
            });
        }
        if ($request->sport_id) {
            $this->query->whereHas('event.sportDiscipline.sport', function ($query) use ($request) {
                $query->where('id', $request->sport_id);
            });
        }
    }
    public function addAppend()
    {
        $this->query->with('event', 'event.sportDiscipline', 'event.sportDiscipline.sport');
    }
    public function deleteList(Request $request)
    {
        try {
            DB::beginTransaction();
            if ($request->action == 'force') {
                $id = json_decode($request->ids);

                $delete = Foul::whereIn('id', $id)->forceDelete();
                DB::commit();
                return $this->deleteResultResponse($delete);
            } else {
                $id = json_decode($request->ids);

                $delete = Foul::whereIn('id', $id)->delete();
                DB::commit();
                return $this->deleteResultResponse($delete);
            }
        } catch (\Exception $e) {
            DB::rollBack();
            return $this->errorResponseSystem($e);
        }
    }

    public function exportData(Request $request)
    {
        $id = json_decode($request->ids);
        $header = [];
        $fields = [];
        $columns = json_decode($request->columns, true);
        foreach ($columns as $column) {
            if ($column['field'] == 'flag') {
            } else {
                $fields[] = $column['field'];
                $header[] = $column['title'];
            }
        }
        $data = [];
        $foul = $this->query->whereIn('id', $id)->with(['event',  'event.sportDiscipline', 'event.sportDiscipline.sport'])->get();
        foreach ($foul as $key => $value) {
            // $data[$key]['No']=$key+1;
            foreach ($fields as $field) {
                $data[$key][$field] = $value[$field];

                if ($field == 'event') {
                    if (App::getLocale() == "vi") {
                        $data[$key]['event'] = $value['event']['name'];
                    } else {
                        $data[$key]['event'] = $value['event']['english_name'];
                    }
                }
                if ($field == 'sport_discipline') {
                    if (App::getLocale() == "vi") {
                        $data[$key]['sport_discipline'] = $value['event']['sportDiscipline']['name'];
                    } else {
                        $data[$key]['sport_discipline'] = $value['event']['sportDiscipline']['english_name'];
                    }
                }
                if ($field == 'sport') {
                    if (App::getLocale() == "vi") {
                        $data[$key]['sport'] = $value['event']['sportDiscipline']['sport']['name'];
                    } else {
                        $data[$key]['sport'] = $value['event']['sportDiscipline']['sport']['english_name'];
                    }
                }
            }
        }
        return Excel::download(new DataExport($id, $header, $data), 'foul.xlsx');
    }
}
